def match_industries(text: str) -> list[str]:
    """text 에서 산업 keyword 매칭 후 관련 ticker 확장 (중복 제거)."""
    if not text:
        return []
    seen: set[str] = set()
    hits: list[str] = []
    for keyword, tickers in INDUSTRY_MAP.items():
        if keyword in text:
            for t in tickers:
                if t not in seen:
                    seen.add(t)
                    hits.append(t)
    return hits
